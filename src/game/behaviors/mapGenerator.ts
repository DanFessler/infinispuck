class MapGenerator extends Behavior {
  camera: { x: number; y: number };

  start() {
    this.camera = this.entity.game.cameraPos;
    const map: Tilemap = this.entity.Tilemap;

    let chunk = chunks[0];
    for (let y = 0; y < 15; y++) {
      for (let x = 0; x < map.width; x++) {
        map.data.set(x, y, chunk[y][x] ? 0 : null);
        // map.data.set(x, y, Math.floor(Math.random() * 3));
      }
    }
    for (let y = 0 + 15; y < 15 + 15; y++) {
      for (let x = 0; x < map.width; x++) {
        map.data.set(x, y, chunk[y - 15][x] ? 0 : null);
        // map.data.set(x, y, Math.floor(Math.random() * 3));
      }
    }
  }

  update() {}
}

