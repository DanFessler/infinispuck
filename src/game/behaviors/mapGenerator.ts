class MapGenerator extends Behavior {
  camera: { x: number; y: number };
  map: Tilemap;

  start() {
    this.camera = this.entity.game.cameraPos;
    this.map = this.entity.Tilemap;

    this.fillChunk(0, chunks[0]);
    this.fillChunk(15, chunks[1]);
  }

  fillChunk(starty: number, chunk: number[][]) {
    for (let y = 0 + starty; y < 15 + starty; y++) {
      for (let x = 0; x < this.map.width; x++) {
        this.map.set(x, y, chunk[y - starty][x]);
      }
    }
  }

  shift() {
    this.entity.position.y -= 16 * 15;
    let deleted = this.map.data.splice(15, 15);

    let newChunk = chunks[Math.floor(Math.random() * 2)];
    this.map.data.splice(0, 0, ...newChunk);
  }

  update() {}
}

export default MapGenerator;
